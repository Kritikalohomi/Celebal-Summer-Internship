--DISPLAYING THE TABLE EMPLOYEES
SELECT * FROM EMPLOYEES

--QUERY EXECUTION 
SELECT 
  CEILING(
    AVG(Salary * 1.0) 
    - 
    AVG(CAST(REPLACE(CAST(Salary AS VARCHAR), '0', '') AS FLOAT))
  ) AS Error_Amount
FROM Employees;
